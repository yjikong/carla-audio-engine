class Data():
    def __init__(self, param: String, value):
        self.param = param
        self.value = value

    def get_param(self):
        return self.param
    
    def set_param(self, param):
        self.param = param

    def get_value(self):
        return self.value

    def set_value(self, value):
        self.value = value    
